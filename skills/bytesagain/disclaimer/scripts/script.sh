cmd_product() {
    cat << 'EOF'
=== Product Disclaimers & Warranties ===

Warranty Types (UCC Article 2):

  Express Warranty:
    Created by seller's statements, descriptions, or samples
    Cannot be disclaimed once made
    Example: "This product will last 5 years"

  Implied Warranty of Merchantability:
    Product is fit for ordinary purpose
    Automatically applies unless disclaimed
    Must be conspicuous to disclaim (CAPS or bold)

  Implied Warranty of Fitness for Particular Purpose:
    When seller knows buyer's specific intended use
    Can be disclaimed with general language

Disclaiming Implied Warranties:
  UCC requires the disclaimer to be CONSPICUOUS:

  "THE PRODUCT IS PROVIDED 'AS IS' WITHOUT WARRANTY OF ANY
  KIND, EXPRESS OR IMPLIED, INCLUDING BUT NOT LIMITED TO THE
  IMPLIED WARRANTIES OF MERCHANTABILITY AND FITNESS FOR A
  PARTICULAR PURPOSE."

  Must be:
  - In writing (for merchantability disclaimer)
  - Conspicuous (caps, bold, different color, larger font)
  - Specifically mention "merchantability" to disclaim it

Limitation of Remedies:
  "IN NO EVENT SHALL [COMPANY] BE LIABLE FOR ANY INDIRECT,
  INCIDENTAL, SPECIAL, CONSEQUENTIAL, OR PUNITIVE DAMAGES."

  "OUR TOTAL LIABILITY SHALL NOT EXCEED THE PURCHASE PRICE
  OF THE PRODUCT."

Product Liability Disclaimers:
  - Cannot fully disclaim personal injury liability
  - Warning labels are crucial (duty to warn)
  - Age restrictions if applicable
  - Proper use instructions
  - Known hazards and contraindications

Software-Specific (common in open source):
  - "AS IS" without warranty of any kind
  - No liability for data loss
  - No guarantee of bug-free operation
  - No guaranteed support or updates
  - Use at your own risk

Digital Product Disclaimers:
  - No guarantee of compatibility with all devices
  - Results may vary based on implementation
  - No refund after download (where permitted)
  - Intellectual property remains with creator
  - License terms govern use
EOF
}

cmd_regulatory() {
    cat << 'EOF'
=== Regulatory Disclaimers ===

FTC (Federal Trade Commission):

  Endorsement Disclaimers:
  - Material connections must be disclosed
  - "Paid partnership" or "Sponsored"
  - "#ad" or "#sponsored" on social media
  - Must be clear and conspicuous
  - Near the endorsement (not buried elsewhere)

  Affiliate Disclaimers:
  - "This post contains affiliate links"
  - Disclose before the first affiliate link
  - Must be noticeable (not hidden)
  - Apply to: blogs, reviews, social posts, videos

  Testimonial Disclaimers:
  - "Results may vary"
  - "Individual results not guaranteed"
  - Cannot use atypical results without noting typical
  - Must reflect genuine experience

SEC (Securities and Exchange Commission):

  Investment Disclaimers:
  - "Securities involve risk, including loss of principal"
  - Forward-looking statements disclaimer
  - Past performance disclaimers
  - Conflicts of interest disclosure
  - Registered advisor disclosure

FDA (Food and Drug Administration):

  Supplement Disclaimers:
  - "These statements have not been evaluated by the FDA"
  - "This product is not intended to diagnose, treat,
     cure, or prevent any disease"
  - Required for all dietary supplement claims
  - Must be near health-related claims

GDPR (EU Data Protection):
  - Data processing disclosures
  - Legal basis for processing
  - Data subject rights
  - International transfer mechanisms
  - Data retention periods
  - Data protection officer contact

CAN-SPAM:
  - Commercial email identification
  - Opt-out mechanism
  - Physical address of sender
  - Valid subject lines (no deception)

DMCA:
  - Safe harbor notice for user-generated content
  - Designated DMCA agent information
  - Takedown procedure description
  - Counter-notification process
EOF
}

cmd_drafting() {
    cat << 'EOF'
=== Disclaimer Drafting Guidelines ===

Language Principles:

  DO:
  ✓ Use plain language (8th-grade reading level)
  ✓ Be specific about what is disclaimed
  ✓ Use active voice where possible
  ✓ Break into short, readable paragraphs
  ✓ Define key terms
  ✓ Use headings and structure
  ✓ Date the disclaimer (effective date)

  DON'T:
  ✗ Use unnecessary legal jargon
  ✗ Make contradictory statements
  ✗ Disclaim things your marketing promises
  ✗ Copy-paste without understanding
  ✗ Disclaim liability for your own negligence
  ✗ Use overly broad catch-all language
  ✗ Forget to update when business changes

Structure Template:
  1. Scope (what this disclaimer covers)
  2. Content accuracy (limitations)
  3. Professional advice disclaimer (if applicable)
  4. Third-party links/content
  5. Warranty exclusions
  6. Liability limitations
  7. Governing law
  8. Changes to disclaimer
  9. Contact information
  10. Effective date

Common Pitfalls:

  1. Inconsistency with Marketing
     Marketing says: "Guaranteed results!"
     Disclaimer says: "No guarantees of any kind"
     → Disclaimer may be voided by conflicting promises

  2. Burying the Disclaimer
     Hidden in page 47 of terms nobody reads
     → Courts may find it unenforceable

  3. Disclaiming Non-Disclaimable Things
     "Not liable for personal injury" (can't disclaim in most places)
     "Not liable for fraud" (cannot disclaim)
     → Undermines credibility of entire disclaimer

  4. One-Size-Fits-All
     Using same disclaimer across different jurisdictions
     → Different laws, different requirements

  5. Set and Forget
     Never updating after business model changes
     → Outdated disclaimers may not protect current activities

Jurisdiction Considerations:
  US       Generally pro-freedom-of-contract, enforces click-wrap
  EU       Consumer protection limits, GDPR, unfair terms directive
  UK       Consumer Rights Act 2015, CMA guidelines
  Australia ACL prohibits unfair contract terms
  Canada    Provincial consumer protection laws vary
EOF
}

