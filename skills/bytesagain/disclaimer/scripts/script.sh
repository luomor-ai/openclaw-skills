cmd_intro() {
    cat << 'EOF'
=== Disclaimers Overview ===

A disclaimer is a statement intended to limit liability, specify
the scope of rights and obligations, or clarify the nature of content
being provided.

Purpose of Disclaimers:
  1. Limit legal liability for the provider
  2. Set user expectations about content quality
  3. Comply with regulatory disclosure requirements
  4. Clarify intellectual property rights
  5. Disclose material relationships (affiliates, sponsors)
  6. Protect against misinterpretation of advice

Legal Basis:
  Contract Law     Disclaimers as terms of use (click-wrap/browse-wrap)
  UCC Article 2    Warranty disclaimers in product sales
  Tort Law         Limiting negligence liability
  Consumer Law     FTC disclosure requirements
  Securities Law   SEC-mandated investment disclaimers
  Professional Law License-specific disclaimer requirements

Enforceability Factors:
  ✓ Conspicuous placement (not buried in fine print)
  ✓ Clear, plain language (not overly legalistic)
  ✓ User had opportunity to read before engaging
  ✓ Reasonable scope (not disclaiming everything)
  ✓ Consistent with actual practices
  ✗ Cannot disclaim gross negligence or fraud
  ✗ Cannot override statutory consumer rights
  ✗ Cannot disclaim liability for personal injury (most jurisdictions)
  ✗ Unconscionable terms may be struck down

Key Legal Terms:
  "As Is"              No warranties of any kind
  "For informational   Not professional advice
   purposes only"
  "Use at your         User assumes all risk
   own risk"
  "No warranty"        Excludes implied warranties
  "Limitation of       Caps financial exposure
   liability"
  "Hold harmless"      User agrees not to sue
EOF
}

cmd_types() {
    cat << 'EOF'
=== Disclaimer Types ===

1. General Disclaimer
   Broad limitation of liability for website/service
   "This website is provided 'as is' without warranties."

2. Professional Advice Disclaimer
   Clarifies content is not professional advice
   "Nothing on this site constitutes medical/legal/financial advice."

3. Warranty Disclaimer
   Excludes implied warranties (merchantability, fitness)
   Required by UCC to be conspicuous (CAPS or bold)

4. Liability Limitation
   Caps maximum damages recoverable
   "Our total liability shall not exceed fees paid in last 12 months."

5. Affiliate/Sponsorship Disclaimer
   FTC-required disclosure of material relationships
   "This post contains affiliate links. I may earn a commission."

6. Copyright/IP Disclaimer
   Clarifies ownership and permitted use
   "All trademarks are property of their respective owners."

7. Fair Use Disclaimer
   For educational/commentary use of copyrighted material
   "This content is used for commentary under fair use."

8. Views & Opinions Disclaimer
   Personal views vs organizational positions
   "Views expressed are the author's own, not the employer's."

9. Investment Disclaimer
   SEC-required for financial content
   "Past performance does not guarantee future results."

10. Medical Disclaimer
    For health-related content
    "This is not medical advice. Consult your healthcare provider."

11. Testimonial Disclaimer
    FTC-required for endorsements
    "Results may vary. Testimonials are not guarantees."

12. AI/Generated Content Disclaimer
    For AI-produced content
    "This content was generated by AI and may contain errors."

13. External Links Disclaimer
    Limiting liability for linked content
    "We are not responsible for content on external websites."

14. Errors & Omissions (E&O)
    For content accuracy
    "While we strive for accuracy, we make no guarantees."

15. Confidentiality Disclaimer
    For emails and communications
    "This message is confidential. If received in error, delete."
EOF
}

cmd_website() {
    cat << 'EOF'
=== Website Disclaimers ===

Essential Website Disclaimers:

  1. Terms of Use / Terms of Service
     - Governs use of the website
     - Includes acceptable use policy
     - Specifies governing law and jurisdiction
     - Contains dispute resolution mechanism

  2. Privacy Policy (legally required in most jurisdictions)
     - What data is collected
     - How data is used and shared
     - User rights (access, deletion)
     - Cookie policy
     - Required by: GDPR, CCPA, CalOPPA, COPPA

  3. General Disclaimer
     - Content accuracy limitations
     - "As is" provision
     - No guarantee of availability
     - Right to modify content

  4. Cookie Notice
     - Types of cookies used
     - Purpose of each cookie
     - How to manage/disable cookies
     - Required by: GDPR, ePrivacy Directive

Placement Best Practices:
  Footer link     Persistent access from every page
  Banner/popup    For cookie consent (before cookies set)
  Registration    Terms checkbox during signup
  Checkout        Terms link near payment button
  Blog posts      Disclaimer at top or bottom of articles
  Landing pages   Near forms that collect data

Click-Wrap vs Browse-Wrap:
  Click-wrap (stronger):
    User must affirmatively agree (check box, click "I agree")
    Generally enforceable in courts
    Use for: signups, purchases, downloads

  Browse-wrap (weaker):
    Terms posted via hyperlink, no active consent required
    Enforceability varies — depends on conspicuousness
    Use for: informational websites, blogs

Browsewrap Enforcement Tips:
  - Place link in prominent location (not just footer)
  - Use contrasting color for the link
  - Reference terms at key interaction points
  - Log user access patterns (prove they were exposed)
EOF
}

